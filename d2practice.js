/*
  - Number: is a data type in JavaScript which represents numeric data
  - Addition Operator (+): Used to add two numbers together or concatenate two strings
  - Subtraction Operator (-): Used to subtract two numbers
  - Multiplication Operator (*): Used to multiply two numbers
  - Division Operator (/): Used to divide two numbers
  - Increment Operator (++): Used to increase a number by one
  - Decrement Operator (--): Used to decrease a number by one
  - Decimal / Floating Points / Floats (.): Used to store decimal numbers
  - Modulus Operator (%): Used to get the remainder of the division of two numbers.
  A number can be checked to be even or odd by checking the remainder of division of
  the number by 2. Even numbers have a remainder of 0 while odd numbers have a rema-
  inder of 1
  */

